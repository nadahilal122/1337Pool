int	ft_sqrt(int nb)
{
	long	r;

	r = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb >= r)
	{
		while (r * r <= nb)
		{
			if (r * r == nb)
				return (r);
			r++;
		}
	}
	return (0);
}
